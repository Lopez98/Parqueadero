public class Area {
    
    private int k_area;
    private int tipo;
    private int cupo_disp;
    private int k_parqueadero;
    
    public Area(){
        
    }

    public int getK_area() {
        return k_area;
    }

    public void setK_area(int k_area) {
        this.k_area = k_area;
    }

    public int getTipo() {
        return tipo;
    }

    public void setTipo(int tipo) {
        this.tipo = tipo;
    }

    public int getCupo_disp() {
        return cupo_disp;
    }

    public void setCupo_disp(int cupo_disp) {
        this.cupo_disp = cupo_disp;
    }

    public int getK_parqueadero() {
        return k_parqueadero;
    }

    public void setK_parqueadero(int k_parqueadero) {
        this.k_parqueadero = k_parqueadero;
    }
    
    
}
